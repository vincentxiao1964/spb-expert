const getLocale = () => {
  return wx.getStorageSync('language') || 'zh';
};

const setLocale = (lang) => {
  wx.setStorageSync('language', lang);
};

const getT = (key) => {
  const locale = getLocale();
  const content = locales[locale][key];
  if (content === undefined) {
    console.warn(`Translation missing for key: ${key} in locale: ${locale}`);
    return key;
  }
  return content;
};

function updateTabBar(instance) {
  const t = (key) => getT(key);
  wx.setTabBarItem({
    index: 0,
    text: t('tabbar_home')
  });
  wx.setTabBarItem({
    index: 1,
    text: t('tabbar_ships')
  });
  wx.setTabBarItem({
    index: 2,
    text: t('tabbar_news')
  });
  wx.setTabBarItem({
    index: 3,
    text: t('tabbar_forum')
  });
  wx.setTabBarItem({
    index: 4,
    text: t('tabbar_mine')
  });
}

module.exports = {
  getLocale: getLocale,
  setLocale: setLocale,
  t: getT,
  updateTabBar: updateTabBar,
  locales: locales
};
